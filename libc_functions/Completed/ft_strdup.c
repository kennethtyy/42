#include "libft.h"
char *ft_strdup(const char *s)
{
	char * s2;
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	s2 = (char *) malloc ((i * sizeof(char)) + 1);
	i = 0;
	while (s[i] != '\0')
	{
		s2[i] = s[i];
		i++;
	}
	return s2;
}
