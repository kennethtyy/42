#include "libft.h"

void	*ft_memset(void *s, int c, size_t m)
{
	size_t	i;
	char	*s2;
	char	c2;

	c2 = (char) c;
	i = 0;
	s2 = s;
	while (i < m)
	{
		s2[i] = c2;
		i++;
	}
	return (s);
}
