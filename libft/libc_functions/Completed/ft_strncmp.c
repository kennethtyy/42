#include "libft.h"

int ft_strncmp(const char *s1, char *s2, size_t c)
{
	size_t i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && i < c)
	{
		if (s1[i] > s2[i])
		{
			return -1;
		}
		else if (s1[i] < s2[i])
		{
			return 1;
		}
		else
		{
			i++;
		}
	}
	return 0;
}
